def printline(ty, nos):
    if ty == 0:
        for x in range(nos):
            print("------------------------------------------------------------------")
    
    if ty == 1:
        for x in range(nos):
            print("++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++")
    
    if ty == 2:
        for x in range(nos):
            print("+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=+=")
    
    if ty == 3:
        for x in range(nos):
            print("==================================================================")
    
    if ty == 4:
        for x in range(nos):
            print("#*#*#*#*#*#*#*#*#*#*#*#*#*#*#*#*#*#*#*#*#*#*#*#*#*#*#*#*#*#*#*#*#*")


